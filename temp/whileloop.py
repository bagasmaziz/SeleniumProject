# WHILE LOOP
a = 3

# block while akan dieksekusi
# selama a > 3
while a > 0:
    print("a = ", a, "> 0")
    a = a - 1 # a awal 3 akan dikurangi 1, a baru = 2
    print("a sekarang : ", a)
    
print("-----------------------------")

# contoh INFINITE LOOP
b = 1   # b akan selalu bernilai 1

while b > 0:
    print("infinite loop")